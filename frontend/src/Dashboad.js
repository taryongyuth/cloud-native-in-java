class Dashboard extends React.Component {
  state = {
    open: false,
    data: [],
    type: 'gold',
  };

  constructor(props) {
    super(props);
    this.getZeroCurveByType(this.state.type)
  }

  handleChange = event => {
    this.getZeroCurveByType(event.target.value)
    this.setState({ [event.target.name]: event.target.value });
    console.log(this.state.type);    
  };

  getZeroCurveByType(type){
    var self = this;
    axios.get('http://localhost:9999/api/calculate/zero-curve?type=' + type)
    .then(function (response) {
      console.log(response.data);
      self.setState({data: response.data})
    })
    .catch(function (error) {
      console.log(error);
    });
  }

  handleDrawerOpen = () => {
    this.setState({ open: true });
  };

  handleDrawerClose = () => {
    this.setState({ open: false });
  };

  render() {
    const { classes } = this.props;

    return (
      <div className={classes.root}>
        <CssBaseline />
        <AppBar
          position="absolute"
          className={classNames(classes.appBar, this.state.open && classes.appBarShift)}
        >
          <Toolbar disableGutters={!this.state.open} className={classes.toolbar}>
            <IconButton
              color="inherit"
              aria-label="Open drawer"
              onClick={this.handleDrawerOpen}
              className={classNames(
                classes.menuButton,
                this.state.open && classes.menuButtonHidden,
              )}
            >
              <MenuIcon />
            </IconButton>
            <Typography
              component="h1"
              variant="h6"
              color="inherit"
              noWrap
              className={classes.title}
            >
              Data Analytics Dashboard
            </Typography>
            <IconButton color="inherit">
              <Badge badgeContent={0} color="secondary">
                <AccountCircle />
              </Badge>
            </IconButton>
          </Toolbar>
        </AppBar>
        <Drawer
          variant="permanent"
          classes={{
            paper: classNames(classes.drawerPaper, !this.state.open && classes.drawerPaperClose),
          }}
          open={this.state.open}
        >
          <div className={classes.toolbarIcon}>
            <IconButton onClick={this.handleDrawerClose}>
              <ChevronLeftIcon />
            </IconButton>
          </div>
          <Divider />
          <List>{mainListItems}</List>
          <Divider />
          <List>{secondaryListItems}</List>
        </Drawer>
        <main className={classes.content}>
          <div className={classes.appBarSpacer} />
          <h2>Select</h2>
          <FormControl variant="outlined" className={classes.formControl}>
          <InputLabel
            ref={ref => {
              this.InputLabelRef = ref;
            }}
            htmlFor="outlined-age-simple"
          >
            Type
          </InputLabel>
          <Select
            value={this.state.type}
            onChange={this.handleChange}
            input={
              <OutlinedInput
                labelWidth={this.state.labelWidth}
                name="type"
                id="outlined-age-simple"
              />
            }
          >
            <MenuItem value={'gold'}>Gold</MenuItem>
            <MenuItem value={'silver'}>Silver</MenuItem>
            <MenuItem value={'platinum'}>Platinum</MenuItem>
          </Select>
        </FormControl>

          {/* <Typography variant="h4" gutterBottom component="h2">
            Future Price
          </Typography>
          <div className={classes.tableContainer}>
            <SimpleTable />
          </div> */}
          
          <Typography component="div" className={classes.chartContainer}>
            <SimpleLineChart data={this.state.data}/>
          </Typography>
        </main>
      </div>
    );
  }
}

Dashboard.propTypes = {
  classes: PropTypes.object.isRequired,
};

export default withStyles(styles)(Dashboard);